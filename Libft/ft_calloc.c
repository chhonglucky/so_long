#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*tmp;

	tmp = malloc(size * count);
	if (tmp == 0)
		return (0);
	ft_memset(tmp, 0, size * count);
	return (tmp);
}
